'use strict';

/**
 * функция сложения чисел
 * @param {number} a 
 * @param {number} b
 * @returns {number} возвращает результат сложения двух чисел 
 */
function Sum(a, b){
    return (a + b);
}

/**
 * функция вычитания чисел
 * @param {number} a 
 * @param {number} b
 * @returns {number} возвращает результат вычитания двух чисел 
 */

function Subtraction(a, b){
    return (a - b);
}

/**
 * функция деления чисел
 * @param {number} a 
 * @param {number} b
 * @returns {number} возвращает результат деления двух чисел 
 */

function Division(a, b){
    return (a / b);
}

/**
 * функция умножения чисел
 * @param {number} a 
 * @param {number} b
 * @returns {number} возвращает результат умножения двух чисел 
 */

function Multiplication(a, b){
    return (a * b);
}
